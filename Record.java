public class Record 
{
    String name;
    int grade;
    
    //Constructor
    public Record(){}
    //getter function that returns name.
    public String getName()
    {
        return name;
    }
    
    //getter function that returns grade.
    public int getGrade()
    {
        return grade;
    }
    
    //Setter function that sets the name.
    public void setName(String name)
    {
        this.name=name;
    }
    
    //setter function that sets the grade.
    public void setGrade(int grade)
    {
        this.grade=grade;
    }
    
   
    
}
